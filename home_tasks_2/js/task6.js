let a = parseFloat(prompt("Введіть число a", ""));
let b = parseFloat(prompt("Введіть число b", ""));
let c = parseFloat(prompt("Введіть число c", ""));
const t = a;
a = b;
b = c;
c = t;
document.write(
  `Згідно алгоритму, який міняє місцями значення змінних наступним чином: 
  змінна a набуває значення змінної b, змінна b набуває значення змінної c, 
  а змінна  c – попереднє значення змінної a, маємо: <br/>
  a = ${a}, b = ${b}, c = ${c}.`
);
